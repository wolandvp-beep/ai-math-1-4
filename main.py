@app.post("/")
async def proxy(request: Request):
    try:
        data = await request.json()
        action = data.get("action")

        if action != "explain":
            return {"error": "Invalid action"}

        user_text = (data.get("text") or "").strip()
        if not user_text:
            return {"error": "Пустой текст задачи"}

        payload = {
            "model": "deepseek-chat",
            "messages": [
                {
                    "role": "system",
                    "content": SYSTEM_PROMPT,
                },
                {
                    "role": "user",
                    "content": (
                        "Сначала определи тип задания. "
                        "Потом объясни решение как сильный школьный учитель: "
                        "простыми фразами, с понятным методом именно для этого типа задания. "
                        "Для уравнений обязательно объясняй перенос через знак равно, "
                        "смену действия и короткую проверку. "
                        "Для текстовых задач объясняй выбор действия по смыслу задачи. "
                        "Для примеров, дробей и геометрии используй подходящий именно им способ объяснения. "
                        "Не используй слабые общие шаблоны.\n\n"
                        f"{user_text}"
                    ),
                },
            ],
            "max_tokens": 1000,
            "temperature": 0.1,
        }

        return await call_deepseek(payload, timeout_seconds=45.0)

    except httpx.ReadTimeout:
        return {"error": "DeepSeek timeout: сервер не дождался ответа от API"}
    except httpx.ConnectTimeout:
        return {"error": "DeepSeek connect timeout: сервер не смог подключиться к API"}
    except httpx.ConnectError as e:
        return {"error": f"DeepSeek connect error: {str(e)}"}
    except Exception as e:
        return {"error": f"Server exception: {str(e)}"}
