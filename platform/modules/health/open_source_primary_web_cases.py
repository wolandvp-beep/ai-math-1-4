def _case(
    name: str,
    block_key: str,
    grade: int | None,
    section_key: str,
    text: str,
    expected_answer_contains: str,
    source_basis_key: str,
    *,
    min_solution_steps: int = 0,
) -> dict[str, Any]:
    block_label = f'{grade} класс' if grade is not None else 'Отдельно · текстовые задачи в 1–2–3 действия'
    return {
        'name': name,
        'block_key': block_key,
        'block_label': block_label,
        'grade': grade,
        'section_key': section_key,
        'section': SECTION_LABELS[section_key],
        'category': f'{block_label} · {SECTION_LABELS[section_key]}',
        'text': text,
        'expected_answer_contains': expected_answer_contains,
        'source_basis_key': source_basis_key,
        'source_basis': SOURCE_BASIS_LABELS[source_basis_key],
        'forbid_source_prefixes': BAD_SOURCE_PREFIXES,
        'required_result_markers': REQUIRED_RESULT_MARKERS,
        'min_solution_steps': min_solution_steps,
    }


